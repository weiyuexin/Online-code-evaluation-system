$(document).ready(function () {
    /*
   * 判断用户是否登录
   * */
    $.ajax({
        url: "/user/check",  //请求地址
        dataType: "json",  //数据格式
        type:"GET",  //请求方式
        async: false,
        success:function (data){
            if(data.flag){ //处于登录状态
               $("#username").html(data.data.username);

            }else {  //没有处于登录状态

            }
        },
        error:function (data) {
            layer.msg("服务异常，请联系管理员");
        }

    });

});